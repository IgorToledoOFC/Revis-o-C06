package Ex001;

public class Membros {
    private String nome;
    private String funcao;

    // Constructor

    public Membros(String nome, String funcao) {
        this.nome = nome;
        this.funcao = funcao;
    }

    // Getters and Setters

    public String getNome() {
        return nome;
    }

    public String getFuncao() {
        return funcao;
    }
}
