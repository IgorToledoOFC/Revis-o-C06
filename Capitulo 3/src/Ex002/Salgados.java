package Ex002;

public class Salgados {
    private String name;
    private double price;

    // Constructor

    public Salgados(String name, double price) {
        this.name = name;
        this.price = price;
    }

    // Getter and setter

    public void setName(String name) {
        this.name = name;
    }

    public void setPrice(double price) {
        this.price = price;
    }
}
